def uppercase_string(self, input_string):
    """
    Функция ввода строки и возврата заглавными символами
    """
    return input_string.upper()

def capitalize_words(self, input_string):
    """
    Функция делает заглавными первые буквы каждого слова в строке
    """
    return input_string.title()

